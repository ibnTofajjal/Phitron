#include <stdio.h>

int main()
{

    int rahim;
    scanf("%d", &rahim); // input
    printf("%d\n", rahim); // output

    float karim;
    scanf("%f", &karim); // input
    printf("%.2f\n", karim); // output

    int kamal;
    scanf("%d%%", &kamal); // input
    printf("%d%%", kamal); // output

    return 0;
}