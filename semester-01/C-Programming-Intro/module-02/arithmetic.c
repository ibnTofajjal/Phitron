#include <stdio.h>

int main () {

    int a = 20;
    int b = 10;

    // Addition
    int sum = a + b;
    printf("Sum: %d\n", sum);

    // Subtraction
    int sub = a - b;
    printf("Subtraction: %d\n", sub);

    // Multiplication
    int multi = a * b;
    printf("Multiplication: %d\n", multi);

    // Division
    int div = a / b;
    printf("Division: %d\n", div);

    // Modulus
    int mod = a % b;

    return 0;
};